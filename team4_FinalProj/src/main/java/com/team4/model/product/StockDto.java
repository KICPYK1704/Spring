package com.team4.model.product;

public class StockDto {
	private String stock_no, stock_prod_no, stock_size, stock_color, stock_quantity;

	public String getStock_no() {
		return stock_no;
	}

	public void setStock_no(String stock_no) {
		this.stock_no = stock_no;
	}

	public String getStock_prod_no() {
		return stock_prod_no;
	}

	public void setStock_prod_no(String stock_prod_no) {
		this.stock_prod_no = stock_prod_no;
	}

	public String getStock_size() {
		return stock_size;
	}

	public void setStock_size(String stock_size) {
		this.stock_size = stock_size;
	}

	public String getStock_color() {
		return stock_color;
	}

	public void setStock_color(String stock_color) {
		this.stock_color = stock_color;
	}

	public String getStock_quantity() {
		return stock_quantity;
	}

	public void setStock_quantity(String stock_quantity) {
		this.stock_quantity = stock_quantity;
	}
	
}
