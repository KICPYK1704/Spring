package com.team4.model.cart;

public class CartDto {

	private String cart_no, cart_quantity, cart_regdate;
	private String prod_name, prod_price, pic_url, des_name;
	private String stock_color, stock_size;
	public String getCart_no() {
		return cart_no;
	}
	public void setCart_no(String cart_no) {
		this.cart_no = cart_no;
	}
	public String getCart_quantity() {
		return cart_quantity;
	}
	public void setCart_quantity(String cart_quantity) {
		this.cart_quantity = cart_quantity;
	}
	public String getCart_regdate() {
		return cart_regdate;
	}
	public void setCart_regdate(String cart_regdate) {
		this.cart_regdate = cart_regdate;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getProd_price() {
		return prod_price;
	}
	public void setProd_price(String prod_price) {
		this.prod_price = prod_price;
	}
	public String getPic_url() {
		return pic_url;
	}
	public void setPic_url(String pic_url) {
		this.pic_url = pic_url;
	}
	public String getDes_name() {
		return des_name;
	}
	public void setDes_name(String des_name) {
		this.des_name = des_name;
	}
	public String getStock_color() {
		return stock_color;
	}
	public void setStock_color(String stock_color) {
		this.stock_color = stock_color;
	}
	public String getStock_size() {
		return stock_size;
	}
	public void setStock_size(String stock_size) {
		this.stock_size = stock_size;
	}
	
	
}
