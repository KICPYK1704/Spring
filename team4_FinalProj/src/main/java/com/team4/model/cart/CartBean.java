package com.team4.model.cart;

public class CartBean {
	private String cart_no, cart_stock, cart_buyer, cart_quantity;

	public String getCart_no() {
		return cart_no;
	}

	public void setCart_no(String cart_no) {
		this.cart_no = cart_no;
	}

	public String getCart_stock() {
		return cart_stock;
	}

	public void setCart_stock(String cart_stock) {
		this.cart_stock = cart_stock;
	}

	public String getCart_buyer() {
		return cart_buyer;
	}

	public void setCart_buyer(String cart_buyer) {
		this.cart_buyer = cart_buyer;
	}

	public String getCart_quantity() {
		return cart_quantity;
	}

	public void setCart_quantity(String cart_quantity) {
		this.cart_quantity = cart_quantity;
	}

	
}
